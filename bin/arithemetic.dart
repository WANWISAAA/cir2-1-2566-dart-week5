void main(){
  int x = 10;
  int y =5;
  print("x+y: $x + $y = ${x+y}");  //1.Addition
  print("x-y: $x - $y = ${x-y}");  //2. Subtraction
  print("x*y: $x * $y = ${x*y}"); //3.Multiplication x*y
  print("x/y: $x / $y = ${x/y}"); //4. Divid x/y
  print("x%y: $x % $y = ${x%y}"); //5. Modulus x%y
  print("x~/y: $x ~/ $y = ${x~/y}"); //6. Division x~/y
  print("-(x-y): -($x - $y) = ${-(x-y)}"); //7. Unary Minus -(x-y)
}