void main(){
  int x = 20;
  int y = 15;
  int z = 10;
  int u = 5;
  print("x++ = ${x++}"); //unary increment postfix : x++
  print("++y = ${++y}"); //unary increment prefix : ++y
  print("++y = ${++y}"); //unary increment postfix : ++y
  print("z-- = ${z--}"); //unary Decrement postfix : z--
  print("--u = ${--u}"); //unary Decrement prefix : --u
} 